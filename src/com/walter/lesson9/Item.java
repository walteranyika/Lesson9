package com.walter.lesson9;

public class Item {
	private String position;
	private String title;
    
	public Item(String position, String title) {
		super();
		this.position = position;
		this.title = title;
	}

	public String getPosition() {
		return position;
	}

	public String getTitle() {
		return title;
	}
	

}
